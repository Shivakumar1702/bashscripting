def sum(num1, num2):
    sum = num1 + num2
    return sum

def diff(num1, num2):
    diff = num1 - num2
    return diff

def mul(num1, num2):
    prod = num1 * num2
    return prod

def div(num1, num2):
    div = num1 / num2
    return div

